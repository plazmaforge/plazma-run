/**
 * Returns unimap array by Encoding ID 
 */
int* _lib_unimap_get_map_id(int id) {
    if (id <= 0) {
        return NULL;
    }

    // DOS
    if (id == 866) {
        return (int*) unimap_cp866;
    }

    // WIN
    if (id == 1250) {
        return (int*) unimap_cp1250;
    } else if (id == 1251) {
        return (int*) unimap_cp1251;
    }

    return NULL;
}

int _lib_unimap_find_idx(int* map, size_t len, int ucode) {
    if (!map || len == 0 || ucode < 0) {
        return -1;
    }
    for (int i = 0; i < len; i++) {
        if (ucode == map[i]) {
            return i;
        }
    }
    return -1;
}

////

int* lib_unimap_get_map_id(int id) {
    return _lib_unimap_get_map_id(id);
}

int lib_unimap_get_unimap_id(lib_unimap_t* unimap, int id) {
    int* map = _lib_unimap_get_map_id(id);
    unimap->map = map;
    if (!map) {
        unimap->start = 0;
        unimap->len   = 0;
        return -1;
    }
    unimap->start = 128; // TODO
    unimap->len   = 128; // TODO
    return 0;
}

int lib_unimap_conv_id(int from, int to, char* data, size_t len) {
    if (!data || len == 0) {
        return 0;
    }

    // Get 'from' map
    lib_unimap_t from_map;
    lib_unimap_get_unimap_id(&from_map, from);
    if (!from_map.map || from_map.len == 0) {
        //fprintf(stderr, "From Map not found : %d", from);
        return -11;
    }

    // Get 'to' map
    lib_unimap_t to_map;
    lib_unimap_get_unimap_id(&to_map, to);
    if (!to_map.map || to_map.len == 0) {
        //fprintf(stderr, "To Map not found : %d", to);
        return -12;
    }

    unsigned char c;

    for (int i = 0; i < len; i++) {
        c = (unsigned char) data[i];        
        int icode = c;
        //fprintf(stderr, ">> icode : 0x%02X\n", (unsigned int) icode);

        // 1. icode -> icode
        if (icode < from_map.start) {
            //fprintf(stderr, ">> icode : skip\n");
            continue;
        }

        // 2. icode -> 'from_map'
        int idx = icode - from_map.start;
        //fprintf(stderr, ">> oidx  : %d\n", idx);

        // Get UTF8 code from 'from_map"
        if (idx >= from_map.len) {
            // error: NO_CHR (?)
            //fprintf(stderr, ">> error: NO_CHR (-13)\n");
            return -13;
        }
        int ucode = from_map.map[idx];
        //fprintf(stderr, ">> ucode : 0x%02X\n", (unsigned int) ucode);

        // Find index in 'to_map' by ucode
        int ocode = 0;
        idx = _lib_unimap_find_idx(to_map.map, to_map.len, ucode);
        if (idx < 0) {
            //fprintf(stderr, ">> error: NO_CHR (FFF)\n");
            ocode = NO_CHR;
        } else {
            //fprintf(stderr, ">> oidx  : %d\n", idx);
            ocode = idx + to_map.start;
        }
        //fprintf(stderr, ">> ocode : 0x%02X\n\n", (unsigned int) ocode);

        data[i] = ocode;
    }
    return 0;
}